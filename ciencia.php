<p> A poucos meses após finalizar o maior telescópio do mundo, a China iniciou no último domingo (25) 
as operações do dispositivo voltado a procurar por vida alienígena. 
A novidade, conhecida como FAST (Telescópio Esférico com Abertura de 500 metros), 
supera o recorde que antigamente pertencia ao Observatório Arecibo, localizado em Porto Rico, que tem uma abertura de 305 metros.
A construção do telescópio, localizado na província de Guizhou, exigiu cinco anos de trabalho e um investimento de US$ 180 milhões 
(R$ 583 milhões). Foram empregados na obra 4.450 painéis com 11 metros de largura, cujo objetivo é refletir ondas de rádio interplanetárias em direção
a uma imensa retina suspensa que pesa 30 toneladas.
A construção deve ser usada para procurar por vidas extraterrestres localizadas na Via Láctea e em outras galáxias.
“O potencial do FAST de descobrir uma civilização alienígena vai ser entre 5 a 10 vezes maior que os equipamentos atuais,
já que ele pode observar distâncias maiores e planetas mais escuros”, afirma Peng Bo, do Observatório Astronômico Nacional (NAO).
O custo do telescópio não envolve somente dinheiro e materiais, mas também a habitação de uma parte da população chinesa.
Segundo a agência de notícias estatal Xinhua, mais de 9 mil pessoas vão ter que deixar suas residências para abrir espaço para o
telescópio e para a estrutura de pesquisa necessária para a sua operação.</p>
