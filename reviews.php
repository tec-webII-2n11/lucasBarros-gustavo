        <b>24 de setembro de 1993: o jogo Myst é oficialmente lançado no Macintosh</b><br>
Esse é um clássico de uma época distante de jogos de PC. Myst é um jogo de exploração extremamente imersivo com jogabilidade em 
primeira pessoa que não possui violência, morte, limite de tempo e vários outros recursos “básicos”.
Ele começou como um projeto nada ambicioso de dois irmãos que, antes do sucesso, foram rejeitados pela 
Activision — que deve se arrepender do fato até hoje, pois Myst vendeu que nem água. E, de uma forma um pouco diferente do convencional, 
o Macintosh (da Apple) foi de fato a primeira plataforma do game.
