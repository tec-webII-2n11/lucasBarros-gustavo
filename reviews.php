        <b>23 de setembro de 1993: Sonic CD chega ao Sega CD no Japão</b><br>
O primeiro Sonic em formato de CD-ROM tem tanto orgulho do feito que diz isso no nome. 
Ele foi a estreia de Metal Sonic como um vilão da franquia e de Amy Rose como uma aliada, 
além de introduzir até elementos de viagem no tempo na franquia. Ele foi um sucesso de crítica e 
foi considerado um dos melhores títulos de todo o Sega CD.
