        <p>A Rocksteady Studios, empresa responsável pelos três principais títulos da série Arkham – Batman: Arkham Asylum, City e Knight – não vai mais desenvolver jogos com o personagem da DC Comics. A notícia chega faltando poucos dias para o lançamento de Batman: Arkham VR, jogo de realidade virtual que está oficialmente finalizado e será o último trabalho da companhia envolvendo o Homem-Morcego.
Em entrevista ao Eurogamer, o produtor Dax Ginn disse que se despedir do personagem foi como terminar um relacionamento. A empresa vem trabalhando com ele desde 2009, quando o primeiro título da série foi lançado.
Publicidade

“O humor da equipe está bem dividido. No escritório tem um sentimento de que esse é definitivamente o momento de fazer outra coisa, todo mundo aceitou isso. Mas a gente também conhece o Batman tão bem e estamos tão confortáveis no universo Arkham.”
Planos para o futuro
Depois do sucesso de crítica e público com a série, a Rocksteady ainda não tem planos para o futuro bem definidos, mas Ginn reconhece que eles estão em uma posição extremamente confortável para criar games novos.
“Estamos em uma posição invejável. Temos tempo e espaço para decidir quais serão os próximos passos. Podemos relaxar e pensar como seria o nosso jogo dos sonhos e temos a Warner Bros. para nos apoiar nesse processo.”
A Rocksteady foi comprada pela Time Warner logo depois do lançamento de Batman: Arkham Asylum, em 2009. Batman: Arkham VR chega ao PS4 no dia 1º de outubro.</p>
